class AgentCrypto:
    """
    Cryptographic identity and transaction signing engine for autonomous agents.
    Uses Ed25519 high-performance elliptic curve signatures.
    """
    def __init__(self):
        self.private_key = ed25519.Ed25519PrivateKey.generate()
        self.public_key = self.private_key.public_key()

    def sign_transaction(self, message: str) -> bytes:
        """Signs an outbound state or invoice message using the agent's private key."""
        message_bytes = message.encode('utf-8')
        return self.private_key.sign(message_bytes)

    @staticmethod
    def verify_transaction(public_key, message: str, signature: bytes) -> bool:
        """Verifies the authenticity of an incoming transaction payload."""
        try:
            message_bytes = message.encode('utf-8')
            public_key.verify(signature, message_bytes)
            return True
        except InvalidSignature:
            return False

